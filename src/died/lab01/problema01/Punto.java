package died.lab01.problema01;

public class Punto {
	float ptoX;
	float ptoY;
	
	public Punto (float x, float y) {
		this.ptoX=x;
		this.ptoY=y;
	}

	public float getPtoX() {
		return ptoX;
	}

	public void setPtoX(float ptoX) {
		this.ptoX = ptoX;
	}

	public float getPtoY() {
		return ptoY;
	}

	public void setPtoY(float ptoY) {
		this.ptoY = ptoY;
	}
	

}
